const { execSync } = require('child_process');
const fs = require('fs');
const path = require('path');

const ROOT_DIR = path.resolve(__dirname, '..');
const PACKAGE_JSON_PATH = path.join(ROOT_DIR, 'package.json');
const GENERATED_TS_PATH = path.join(ROOT_DIR, 'src', 'build-info.generated.ts');
const BUILD_META_PATH = path.join(ROOT_DIR, 'build', 'build-info.json');

const mode = process.argv[2] === 'production' ? 'production' : 'development';
const channel = process.argv[3] ?? (mode === 'production' ? 'release' : 'local');

const packageJson = JSON.parse(fs.readFileSync(PACKAGE_JSON_PATH, 'utf8'));

const getGitInfo = () => {
	try {
		const gitSha = execSync('git rev-parse --short HEAD', {
			cwd: ROOT_DIR,
			encoding: 'utf8',
		}).trim();
		const gitDirty = execSync('git status --porcelain', {
			cwd: ROOT_DIR,
			encoding: 'utf8',
		}).trim().length > 0;

		return { gitSha, gitDirty };
	} catch {
		return { gitSha: 'unknown', gitDirty: false };
	}
};

const formatBuildId = (date) => {
	const pad = (value) => String(value).padStart(2, '0');

	return [
		date.getUTCFullYear(),
		pad(date.getUTCMonth() + 1),
		pad(date.getUTCDate()),
		'-',
		pad(date.getUTCHours()),
		pad(date.getUTCMinutes()),
		pad(date.getUTCSeconds()),
	].join('');
};

const builtAt = new Date();
const { gitSha, gitDirty } = getGitInfo();

const buildInfo = {
	version: packageJson.version,
	gitSha,
	gitDirty,
	mode,
	channel,
	buildId: formatBuildId(builtAt),
	builtAt: builtAt.toISOString(),
};

const generatedTs = `/* Auto-generated by scripts/generate-build-info.cjs. Do not edit. */

export type BuildMode = 'development' | 'production';

export type BuildInfo = {
	version: string;
	gitSha: string;
	gitDirty: boolean;
	mode: BuildMode;
	channel: string;
	buildId: string;
	builtAt: string;
};

export const BUILD_INFO: BuildInfo = ${JSON.stringify(buildInfo, null, '\t')};
`;

fs.mkdirSync(path.dirname(BUILD_META_PATH), { recursive: true });
fs.writeFileSync(GENERATED_TS_PATH, generatedTs);
fs.writeFileSync(BUILD_META_PATH, `${JSON.stringify(buildInfo, null, 2)}\n`);

console.log(`Build info: v${buildInfo.version} · ${buildInfo.mode} · ${buildInfo.channel} · ${buildInfo.gitSha}${buildInfo.gitDirty ? '*' : ''}`);
